    private void loginLabelMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginLabelMousePressed
        // TODO add your handling code here:

        String Uname = username.getText();
        String Pswd = password.getText();

        String selectUrl = "select * from users where username = '" + Uname + "' and password = '" + Pswd + "'";

        try {
            pst = conn.prepareStatement(selectUrl);
            rst = pst.executeQuery();
            if (rst.next()) {
                ImageIcon iconic = new ImageIcon(Home.class.getResource("/images/icons8-user.png"));
                JOptionPane.showMessageDialog(null, "Logged In Successfully","Lets Go",JOptionPane.INFORMATION_MESSAGE,iconic);
                usernameIcon.setVisible(false);
                passwordIcon.setVisible(false);
                username.setText("");
                password.setText("");
                
                UserPanel us = new UserPanel();
                us.UserNameDisplay(Uname);
                us.setVisible(true);
                super.dispose();
                conn.close();
                System.out.println("Connection Closed");
                pst.close();
                System.out.println("Prepared Statement Closed");                
            } else {
                ImageIcon iconic = new ImageIcon(Home.class.getResource("/images/icons8-user.png"));
                JOptionPane.showMessageDialog(null, "Invalid Username or Password","Sorry",JOptionPane.INFORMATION_MESSAGE,iconic);
                usernameIcon.setVisible(true);
                passwordIcon.setVisible(true);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_loginLabelMousePressed

