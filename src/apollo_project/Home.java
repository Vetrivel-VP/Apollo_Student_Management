    private void signupBtnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signupBtnMousePressed
        // TODO add your handling code here:

        String fname = textFirst.getText();
        String lname = textLast.getText();
        String uname = textUser.getText();
        String emailID = textEmail.getText();
        String pass = textPass.getText();
        String cpass = textConfirmPass.getText();

        if (fname.equals("") || uname.equals("") || pass.equals("") || cpass.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill the required fields...!");
        } else {
            try {
                String insertUrl = "insert into users (username,firstname,lastname,password,confirm_password,email) values ('" + uname + "','" + fname + "',"
                        + "'" + lname + "','" + pass + "','" + cpass + "','" + emailID + "')";
                pst = conn.prepareStatement(insertUrl);
                pst.execute();
                JOptionPane.showMessageDialog(null, "User Added Successfully");
                textFirst.setText("");
                textLast.setText("");
                textUser.setText("");
                textEmail.setText("");
                textPass.setText("");
                textConfirmPass.setText("");

                emailIconPanel.removeAll();
                emailIconPanel.repaint();
                emailIconPanel.revalidate();

                passIconPanel.removeAll();
                passIconPanel.repaint();
                passIconPanel.revalidate();

                confirmPassIconPanel.removeAll();
                confirmPassIconPanel.repaint();
                confirmPassIconPanel.revalidate();

                invalidEmailPanel.setVisible(false);

                passwordWrong.setVisible(false);

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }


    }//GEN-LAST:event_signupBtnMousePressed

